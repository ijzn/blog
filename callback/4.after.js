// 计数器原则

/**
 * ++i 先给i自加1，再计算表达式的值。i++先计算表达式的值，再给i自加1。
 * var i = 1; (++i)+0; // 2 
 * var i = 1; (i++)+0; // 1
 */
let after = (fn,times) =>{ // 生成新的函数等待执行次数达到相应的次数的时候，执行！
    return () => {
        if (--times == 0) {
            fn()
        }
    }
}

let a = after(()=>{
    console.log('---3次');
},3)
a()
a()
